package com.yidu.lly.model;

public class Coltag {

	private int coltid;
	private String tagname;
	private int cid;
	private int taguid;
	public int getColtid() {
		return coltid;
	}
	public void setColtid(int coltid) {
		this.coltid = coltid;
	}
	public String getTagname() {
		return tagname;
	}
	public void setTagname(String tagname) {
		this.tagname = tagname;
	}
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	public int getTaguid() {
		return taguid;
	}
	public void setTaguid(int taguid) {
		this.taguid = taguid;
	}
	
}
