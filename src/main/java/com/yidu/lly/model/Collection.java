package com.yidu.lly.model;

public class Collection {
	private int cid;
	private String collectionname;
	private String collectionintroduce;
	private int uid;
	private String collectionimg;
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	public String getCollectionname() {
		return collectionname;
	}
	public void setCollectionname(String collectionname) {
		this.collectionname = collectionname;
	}
	public String getCollectionintroduce() {
		return collectionintroduce;
	}
	public void setCollectionintroduce(String collectionintroduce) {
		this.collectionintroduce = collectionintroduce;
	}
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	public String getCollectionimg() {
		return collectionimg;
	}
	public void setCollectionimg(String collectionimg) {
		this.collectionimg = collectionimg;
	}
	
}
